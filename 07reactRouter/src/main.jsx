const router = createBrowserRouter(//is a function used for creating routing in a React application using the browser’s URL system.
  createRoutesFromElements(//So createRoutesFromElements lets you define routes using <Route> JSX components, which looks cleaner.
    <Route path='/' element={<Layout />}>
      <Route path='' element={<Home />} />
      <Route path='about' element={<About />} />
      <Route path='contact' element={<Contact />} />
      <Route path='user/:userid' element={<User />} />
      {/*a loader is a function used to fetch data before a page (route) loads.*/}
      <Route 
      loader={githubInfoLoader}
      path='github' 
      element={<Github />}
       />
    </Route>
  )
)

ReactDOM.createRoot(document.getElementById('root')).render(
  <React.StrictMode>
    <RouterProvider router={router} />
  </React.StrictMode>
)